<?php
$a = 10; //Aqui definimos que o valor "$a" vale 10.

echo "<br>";

echo $a++; //Aqui nós vemos o valor de "$a" e somamos mais 1.

echo "<br>";

echo ++$a; //Aqui nós incrementamos direto, nós mudamos antes de fazer a operação.

echo "<br>"; //Aqui nós quebramos a linha.

echo $a; //Aqui mostramos o resultado final (que no caso, deu 11).

echo "<br>";

echo --$a; //Aqui nós decrementamos.

//Site para boas praticas: https://www.php-fig.org/psr/psr-12/
?>
